-- Міграція 003: перейменування ролі-мітки «координатор» → «головний організатор».
-- Нові мітки («ментор», «секретар департаменту», «секретар організації»)
-- додаються без змін схеми — role_label є звичайним VARCHAR.
-- Для існуючих БД:
--   psql $DATABASE_URL -f src/db/migrations/003_role_labels.sql

UPDATE memberships
SET role_label = 'головний організатор'
WHERE role_label = 'координатор';
